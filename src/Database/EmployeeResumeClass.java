package Database;

public class EmployeeResumeClass {
	String manv, tennv, hocvan, chuyenmon, ngoaingu, dantoc, tongiao, quanhe;

	public EmployeeResumeClass(String manv, String tennv, String hocvan, String chuyenmon, String ngoaingu, String dantoc,
			String tongiao, String quanhe) {
		super();
		this.manv = manv;
		this.tennv = tennv;
		this.hocvan = hocvan;
		this.chuyenmon = chuyenmon;
		this.ngoaingu = ngoaingu;
		this.dantoc = dantoc;
		this.tongiao = tongiao;
		this.quanhe = quanhe;
	}

	public String getManv() {
		return manv;
	}

	public void setManv(String manv) {
		this.manv = manv;
	}

	public String getTennv() {
		return tennv;
	}

	public void setTennv(String tennv) {
		this.tennv = tennv;
	}

	public String getHocvan() {
		return hocvan;
	}

	public void setHocvan(String hocvan) {
		this.hocvan = hocvan;
	}

	public String getChuyenmon() {
		return chuyenmon;
	}

	public void setChuyenmon(String chuyenmon) {
		this.chuyenmon = chuyenmon;
	}

	public String getNgoaingu() {
		return ngoaingu;
	}

	public void setNgoaingu(String ngoaingu) {
		this.ngoaingu = ngoaingu;
	}

	public String getDantoc() {
		return dantoc;
	}

	public void setDantoc(String dantoc) {
		this.dantoc = dantoc;
	}

	public String getTongiao() {
		return tongiao;
	}

	public void setTongiao(String tongiao) {
		this.tongiao = tongiao;
	}

	public String getQuanhe() {
		return quanhe;
	}

	public void setQuanhe(String quanhe) {
		this.quanhe = quanhe;
	}
}
